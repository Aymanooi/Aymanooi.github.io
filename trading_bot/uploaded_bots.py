import pandas as pd


# ============ الاستراتيجيات الثلاث (تنفيذ أمين للأصل) ============

def sig_momentum_scalp(df, i):
    """موقع السعر في نطاق 24 شمعة — عتبات 0.85/0.15 (الأصل: ticker 24h)."""
    if i < 24:
        return 0
    w = df.iloc[i - 23:i + 1]
    hi, lo = w["high"].max(), w["low"].min()
    if hi == lo:
        return 0
    m = (df.iloc[i]["close"] - lo) / (hi - lo)
    return 1 if m > 0.85 else (-1 if m < 0.15 else 0)


def sig_sma_trend(df, i):
    """SMA5 مقابل SMA20 بهامش 1% (الأصل: شموع دقيقة)."""
    if i < 20:
        return 0
    closes = df["close"].iloc[i - 19:i + 1]
    s5, s20 = closes.iloc[-5:].mean(), closes.mean()
    return 1 if s5 > s20 * 1.01 else (-1 if s5 < s20 * 0.99 else 0)


def sig_volatility(df, i):
    """تقلب >2.5% → اتبع جهة السعر من المتوسط."""
    if i < 100:
        return 0
    closes = df["close"].iloc[i - 99:i + 1]
    mean = closes.mean()
    if closes.std() / mean * 100 > 2.5:
        return 1 if df.iloc[i]["close"] > mean else -1
    return 0


def sig_advanced_vote(df, i):
    """التصويت بالأغلبية بين الثلاث — قلب okx_advanced_bot."""
    s = [sig_momentum_scalp(df, i), sig_sma_trend(df, i), sig_volatility(df, i)]
    longs, shorts = s.count(1), s.count(-1)
    return 1 if longs > shorts else (-1 if shorts > longs else 0)


